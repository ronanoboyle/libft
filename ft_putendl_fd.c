#include "libft.h"

void	ft_putendl_fd(char *c, int fd)
{
	int	i;

	i = 0;
	if (c != NULL)
	{
		while (c[i] != '\0')
		{
			write(fd, &c[i], 1);
			i++;
		}
		write(fd, "\n", 1);
	}
}
